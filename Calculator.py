first_number = int(input("Enter Your first Number:"))
math_operation = str(input("Enter your math symbol(+-*/):"))
second_number = int(input("Enter Your second Number:"))
if math_operation == ("+"):
    print (first_number+second_number)
if math_operation == ("-"):
    print (first_number-second_number)
if math_operation == ("*"):
    print (first_number*second_number)
if math_operation == ("/"):
    print (first_number/second_number)



